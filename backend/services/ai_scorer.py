async def _call_gemini(system_prompt: str, user_prompt: str) -> str:
    """Вызов Google Gemini API (gemini-1.5-pro) с JSON mode."""
    import google.generativeai as genai

    genai.configure(api_key=os.getenv("GOOGLE_API_KEY"))
    model_name = os.getenv("GOOGLE_MODEL", "gemini-1.5-pro")

    model = genai.GenerativeModel(
        model_name=model_name,
        system_instruction=system_prompt,
        generation_config={
            "temperature": 0.1,
            "response_mime_type": "application/json",
        }
    )

    response = await model.generate_content_async(user_prompt)
    return response.text


async def _call_ollama(prompt: str) -> str:
    """Вызов локальной Ollama (OpenAI-compatible API)."""
    base_url = os.getenv("OLLAMA_BASE_URL", "http://localhost:11434")
    model = os.getenv("OLLAMA_MODEL", "qwen2.5:14b")

    async with httpx.AsyncClient(timeout=300.0) as client:
        resp = await client.post(
            f"{base_url}/api/chat",
            json={
                "model": model,
                "messages": [
                    {"role": "system", "content": SYSTEM_PROMPT},
                    {"role": "user", "content": prompt},
                ],
                "stream": False,
                "format": "json",
                "options": {
                    "temperature": 0.1,   # Низкая температура = стабильный JSON
                    "num_predict": 4096,
                },
            },
        )
        resp.raise_for_status()
        data = resp.json()
        return data["message"]["content"]


async def _call_openai(prompt: str) -> str:
    """Вызов OpenAI API (GPT-4o) с JSON mode."""
    from openai import AsyncOpenAI

    client = AsyncOpenAI(api_key=os.getenv("OPENAI_API_KEY"))
    model = os.getenv("OPENAI_MODEL", "gpt-4o")

    response = await client.chat.completions.create(
        model=model,
        messages=[
            {"role": "system", "content": SYSTEM_PROMPT},
            {"role": "user", "content": prompt},
        ],
        response_format={"type": "json_object"},
        temperature=0.1,
        max_tokens=4096,
    )
    return response.choices[0].message.content


async def _call_anthropic(prompt: str) -> str:
    """Вызов Anthropic Claude API."""
    import anthropic

    client = anthropic.AsyncAnthropic(api_key=os.getenv("ANTHROPIC_API_KEY"))
    model = os.getenv("ANTHROPIC_MODEL", "claude-3-5-sonnet-20241022")

    message = await client.messages.create(
        model=model,
        max_tokens=4096,
        system=SYSTEM_PROMPT + "\n\nОтвечай ТОЛЬКО валидным JSON без markdown-блоков.",
        messages=[{"role": "user", "content": prompt}],
        temperature=0.1,
    )
    return message.content[0].text


