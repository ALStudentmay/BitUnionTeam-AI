async def score_tz(tz_markdown: str) -> TZAuditResult:
    """
    Главная функция скоринга.
    Аргументы:
        tz_markdown: текст ТЗ в формате Markdown
    Возвращает:
        TZAuditResult — строго типизированный результат аудита
    """
    provider = os.getenv("LLM_PROVIDER", "gemini").lower()
    logger.info(f"Запуск скоринга через провайдер: {provider}")

    user_prompt = USER_PROMPT_TEMPLATE.format(tz_markdown=tz_markdown[:12000])  # обрезаем для экономии токенов

    # Выбор провайдера
    if provider == "openai":
        raw_json = await _call_openai(user_prompt)
    elif provider == "anthropic":
        raw_json = await _call_anthropic(user_prompt)
    elif provider == "gemini":
        raw_json = await _call_gemini(SYSTEM_PROMPT, user_prompt)
    else:
        raw_json = await _call_ollama(user_prompt)

    logger.debug(f"Raw LLM response:\n{raw_json[:500]}...")

    # Парсинг JSON (с очисткой от markdown-оберток)
    parsed_data = _extract_json(raw_json)

    # Валидация через Pydantic
    result = TZAuditResult(**parsed_data)

    # Пересчёт total_score на основе критериев (защита от галлюцинаций)
    recalculated = sum(c.score for c in result.criteria)
    if abs(recalculated - result.total_score) > 5:
        logger.warning(
            f"LLM вернула total_score={result.total_score}, "
            f"но сумма критериев={recalculated}. Корректируем."
        )
        result = result.model_copy(update={"total_score": recalculated})

    # Автоматический grade
    grade = _calculate_grade(result.total_score)
    result = result.model_copy(update={"grade": grade})

    logger.info(f"Скоринг завершён: {result.total_score}/100 ({grade})")
    return result


def _extract_json(raw: str) -> dict[str, Any]:
    """Извлекает JSON из текста, даже если LLM обернула его в ```json ... ```."""
    raw = raw.strip()

    # Убираем markdown-обёртки
    if raw.startswith("```"):
        raw = re.sub(r"^```(?:json)?\n?", "", raw)
        raw = re.sub(r"\n?```$", "", raw)
        raw = raw.strip()

    try:
        return json.loads(raw)
    except json.JSONDecodeError:
        # Попытка найти JSON-объект в тексте
        match = re.search(r"\{.*\}", raw, re.DOTALL)
        if match:
            return json.loads(match.group())
        raise ValueError(f"Не удалось извлечь JSON из ответа LLM: {raw[:200]}")


def _calculate_grade(score: int) -> str:
    if score >= 85:
        return "A"
    elif score >= 70:
        return "B"
    elif score >= 55:
        return "C"
    elif score >= 40:
        return "D"
    else:
        return "F"
